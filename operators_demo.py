# Description: examples of Python operators
     
# Addition
a = 10
b = 3
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", a / b)

# Floor Division
print("Floor Division:", a // b)

# Modulus
print("Modulus:", a % b)

# Exponentiation
print("Exponentiation:", a ** b)


# comparison operators example

a = 10
b = 5

print("a == b:", a == b)   # False
print("a != b:", a != b)   # True
print("a > b:", a > b)     # True
print("a < b:", a < b)     # False
print("a >= b:", a >= b)   # True
print("a <= b:", a <= b)   # False


# logical operators example

# Example values
a = 12
b = 7

# Using AND operator
if a > 10 and b < 10:
    print("Both conditions are True!")

# Using OR operator
if a < 10 or b < 10:
    print("At least one condition is True!")

# Using NOT operator
if not (a < 10):
    print("a is not less than 10")





